-- Products
CREATE TABLE products (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR(255) NOT NULL
);

-- Flows
CREATE TABLE flows (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  product_id BIGINT NOT NULL REFERENCES products(id),
  input_id VARCHAR(255),
  input_unit VARCHAR(50),
  input_value DECIMAL(10, 4),
  output_id VARCHAR(255),
  output_unit VARCHAR(50),
  output_value DECIMAL(10, 4)
);

-- Impacts
CREATE TABLE impacts (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  flow_id BIGINT NOT NULL REFERENCES flows(id),
  method_id VARCHAR(255),
  impact_id VARCHAR(255),
  impact_id_unit VARCHAR(50),
  impact_value DECIMAL(10, 4)
);